const normalize = (value = '') => value.toLowerCase().replace(/ё/g, 'е');

function findBuiltInMatch(resource) {
  if (resource.slug) return BUILT_IN_RESOURCES.find(item => item.slug === resource.slug);
  const title = normalize(resource.title_ru);
  return BUILT_IN_RESOURCES.find(item => {
    const builtInTitle = normalize(item.title_ru);
    return title.includes(item.slug.replace(/-/g, ' ')) ||
      title.includes(builtInTitle.split(':')[0]) ||
      builtInTitle.includes(title.split(':')[0]);
  });
}

export function mergeResources(databaseResources = []) {
  const published = databaseResources.filter(resource => resource.is_published !== false);
  const usedIds = new Set();

  const builtIns = BUILT_IN_RESOURCES.map(item => {
    const override = published.find(resource => findBuiltInMatch(resource)?.slug === item.slug);
    if (override?.id) usedIds.add(override.id);
    return { ...item, ...override, slug: item.slug };
  });

  return [...builtIns, ...published.filter(resource => !usedIds.has(resource.id))]
    .sort((a, b) => (a.sort_order || 0) - (b.sort_order || 0));
}

